#definiendo una variable con camelCase
nombreCompleto = "True  False"

#definiendo una variable con snake_case
nombre_completo = "true False"

#concatenar con +
bienvenida = "hola "+ nombreCompleto + " ¿como estas?" 

#concatenar con f strins
bienvenida = f"hola  {nombreCompleto}   ¿como estas?"

#operadores de pertenencia ("in" y "not in")
print("True" not in bienvenida)
print("True" in bienvenida)

#los operadores solo devuelven "True" o "False"
